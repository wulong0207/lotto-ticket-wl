package com.hhly.ticket.service.ticket.dealer.saiwei;

public class Odd {

	private String date;

	private String week;

	private String matchNo;

	private String playingCode;

	private String playingItem;

	private String odds;

	private String letPoint;

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getWeek() {
		return week;
	}

	public void setWeek(String week) {
		this.week = week;
	}

	public String getMatchNo() {
		return matchNo;
	}

	public void setMatchNo(String matchNo) {
		this.matchNo = matchNo;
	}

	public String getPlayingCode() {
		return playingCode;
	}

	public void setPlayingCode(String playingCode) {
		this.playingCode = playingCode;
	}

	public String getPlayingItem() {
		return playingItem;
	}

	public void setPlayingItem(String playingItem) {
		this.playingItem = playingItem;
	}

	public String getOdds() {
		return odds;
	}

	public void setOdds(String odds) {
		this.odds = odds;
	}

	public String getLetPoint() {
		return letPoint;
	}

	public void setLetPoint(String letPoint) {
		this.letPoint = letPoint;
	}

}
